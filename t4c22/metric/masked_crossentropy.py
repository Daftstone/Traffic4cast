import numpy as np


def get_weights_from_class_counts(class_counts):
    n = np.sum(class_counts)
    return [n / (c * 3) for c in class_counts]


def get_weights_from_class_fractions(class_fractions):
    n = np.sum(class_fractions)
    return [n / (c * 3) for c in class_fractions]
